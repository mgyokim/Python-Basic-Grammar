# 정수형
a = 123
print("🚀 ~  ~ a: ", a);

a = -178
print("🚀 ~  ~ a: ", a);
a = 0
print("🚀 ~  ~ a: ", a);

# 실수형
a = 1.2
print("🚀 ~  ~ a: ", a);
a = -3.45
print("🚀 ~  ~ a: ", a);

a = 4.24E10
print("🚀 ~  ~ a: ", a);

a = 0o177 #0o177 = 1 * 8^2 + 7 * 8^1 + 7 * 8^0 = 127
print("🚀 ~  ~ a: ", a); # 8진수 octal

a = 0x8ff
print("🚀 ~  ~ a: ", a);  # 16진수 hexadecial (8 * 16^2) + (15 * 16) + 15 = 2303

b = 0xABC
print("🚀 ~  ~ b: ", b); # (10 * 16^ 2) + (11 * 16) + 12 = 2748

# 사칙 연산
a = 3
b = 4
a + b
print("🚀 ~  ~ a + b: ", a + b);
a - b
print("🚀 ~  ~ a - b: ", a - b);
a * b
print("🚀 ~  ~ a * b: ", a * b);
a / b
print("🚀 ~  ~ a / b: ", a / b);


# x의 제곱을 나타내는 ** 연산자
a = 3
b = 4
print("🚀 ~  ~ a ** b: ", a ** b);

# 나눗셈 후 나머지를 반환하는 % 연산자R
print("🚀 ~  ~ 7 % 3: ", 7 % 3);
print("🚀 ~  ~ 3 % 7: ", 3 % 7);

# 나눗셈 후 몫을 반환하는 // 연산자
print("🚀 ~  ~ 7 / 4: ", 7 / 4);
print("🚀 ~  ~ 7 // 4: ", 7 // 4);

# 복합 연산자
a = 1
a = a + 1
print("🚀 ~  ~ a: ", a);
a += 1
print("🚀 ~  ~ a: ", a);

a -= 1
print("🚀 ~  ~ a: ", a);


